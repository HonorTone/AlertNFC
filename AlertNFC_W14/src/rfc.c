/****************************************************************************
 * runRfcConvertingOperation function.
 *
 * @brief	RFC Converting Operations.
 *
 * @param	startCount		Start measurement count.
 * @param	sensorCount		Pointer variable that stores measurement counter.
 *
 * @retval	RFC_STS_CONV_SEN_A		Sensor A oscillation completion interrupt.
 * @retval	RFC_STS_CONV_ERR_OVTC	Time base counter overflow error.
 * @retval	RFC_STS_CONV_ERR_OVMC	Measurement counter overflow error.
 ***************************************************************************/
int runRfcConvertingOperation( unsigned long startCount, unsigned long* sensorCount )
{
	int result = RFC_STS_CONV_ERR_OVMC;
	/// Set measurement counter data.
	setRfcMeasurementCounter( startCount );
	/// Set Time base counter data.
	setRfcTimeBaseCounter( 0x00000000 );

	/// Clear interrupt flag.
	//RFC0INTF_OVTCIF     = 1;
	//RFC0INTF_EREFIF     = 1;
	RFC0INTF			= 0x0011;

	/// Start CR oscillation.
	//RFC0TRG_SREF		= 1;
	RFC0TRG				|= 0x0001;
	while ( RFC0INTF_EREFIF == 0 ) {
		/// Time base counter overflow error.
		if ( RFC0INTF_OVTCIF == 1 ) {
			//RFC0INTF_OVTCIF = 1;
			RFC0INTF = 0x0010;
			return RFC_STS_CONV_ERR_OVTC;
		}
	}

	/// Clear interrupt flag.
	//RFC0INTF_OVMCIF     = 1;
	//RFC0INTF_ESENAIF    = 1;
	RFC0INTF			= 0x000A;

	/// Start Sensor A oscillation.
	//RFC0TRG_SSENA		= 1;
	RFC0TRG				|= 0x0002;


	/// Wait interrupts.
	while ( 1 ) {
		/// Sensor A oscillation completion interrupt.
		if ( RFC0INTF_ESENAIF == 1 ) {
			//RFC0INTF_ESENAIF = 1;
			RFC0INTF = 0x0002;
			*sensorCount = getRfcMeasurementCounter();
			result = RFC_STS_CONV_SEN_A;
			break;
		}
		/// Measurement counter overflow error.
		if ( RFC0INTF_OVMCIF == 1 ) {
			//RFC0INTF_OVMCIF = 1;
			RFC0INTF = 0x0008;
			result = RFC_STS_CONV_ERR_OVMC;
			break;
		}
	}

	return result;
}

